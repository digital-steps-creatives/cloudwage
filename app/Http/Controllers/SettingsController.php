     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //

        $general = General::first();

    if($general != null)
        {
            $general->update(['days' => $request->days,
                            'hours' => $request->hours,
                            'ot_1' => $request->ot_1,
                            'ot_2' => $request->ot_2
            ]);
            flash('Successfully updated overtime settings', 'success');
            return redirect()->route('general.index');
        }else{
           General::create($request->all());
           flash('Successfully created overtime settings', 'success');
           return redirect()->route('general.index');
        }
    }

